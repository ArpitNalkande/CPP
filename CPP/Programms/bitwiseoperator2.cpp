#include<iostream>
using namespace std;
int main()
{
    // LEFT SHIFT OPERATOR

    int x=3;
    cout<<(x<<1)<<endl;
    cout<<(x<<2)<<endl;

    // RIGHT SHIFT OPERATOR

    int y=33;
    cout<<(y>>1)<<endl;
    cout<<(y>>2)<<endl;

    // BITWISE NOT

    int z=1;
    cout<<(~z)<<endl;

}